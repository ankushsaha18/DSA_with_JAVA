import java.util.*;
public class switchcase {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int inp=sc.nextInt();

        switch(inp){
            case 1 : System.out.println("Hello");
            break;
            case 2 : System.out.println("Namaste");
            break;
            case 3 : System.out.println("Nomoshkar");
            break;
        }

    }
    
}
